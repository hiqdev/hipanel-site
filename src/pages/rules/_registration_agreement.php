			<b>Definitions</b>
			<p><b>'Domain'</b> means a region of space for hierarchical Internet names indicated by a unique domain name, serviced by a set of Domain Name Servers (DNS)
			and administrated by its administrator. ICANN is the Internet Corporation for Assigned Names and Numbers that coordinates the domain name and IP address system,
			defines policies and enables functionality of root servers.</p>
			<p><b>'International domains'</b> are .NET, .COM, .ORG, .BIZ, .INFO domains.</p>
			<p><b>'Top-level domain administrator'</b> is an organization authorized by ICANN to administrate, define the registration policies,
			delegation and functioning of domains in the international top-level domains.</p>
			<p><b>'National domain'</b> is a two-lettered top-level domain assigned by the Internet Assigned Numbers Authority (IANA) with the concurrence of Internet
			societies under country codes relying upon the ISO 3166-1 standard.</p>
			<p><b>'Top-level national domain administrator'</b> is an organization which administrates the top-level national domain assigned for a country or a territory.</p>
			<p><b>'National domains of other countries'</b> are .СС, .LC, .ME, .TV domains.</p>
			<p><b>'Registry'</b> is a central database of the relevant international or national top-level domains with information on the registered domain names and DNS
			servers, other information required for the registration and the delegation of domains.</p>
			<p><b>'Registry Operator'</b> is a legal entity ensuring the functioning of the registry and giving access to the registry to Registrars so that the Registrars provide
			the domain name registration services.</p>
			<p><b>'Premium Domain'</b> is a domain reserved by the top-level domain administrator, top-level national domain administrator, registry operator or ICANN with
			the 'premium' status, the way and/or cost of the registration and/or renewal of which could differ from standard ones.</p>
			<p><b>'Registrar'</b> is <?= $registrarName ?> which provides the domain name registration services and ensures the transfer of the required information to the registry.</p>
			<p><b>'Partner of Registrar'</b> (<b>'Partner'</b>) is a person participating in the partnership program of the Registrar (the party under the agreement on the provision
			of comprehensive services, index EVO-REG).</p>
			<p><b>'Other Registrar'</b> is any legal entity accredited by ICANN or another authorized entity, including the top-level international domain Administrator,
			providing the domain name registration services and ensuring the transfer of the required information to the Registry.</p>
			<p><b>'Domain name registration'</b> is entering the information on the domain name to the Registry by the Registrar. The term registration, during which
			the information on the domain name is recorded in the Registry, is defined by the domain name Administrator, taking into account the longest and the
			smallest possible terms of the registration set by the applicable Registry. The domain name registration may be renewed at the sole discretion of
			the domain name Administrator.</p>
			<p><b>'Domain name Administrator'</b> is a person to the name of whom the domain name is registered. The domain name Administrator is defined in the
			Registrant field in the information on the domain name. The domain name Administrator defines a procedure for the use of the domain name, takes
			liabilities to pay for the registration and the renewal of the registration term, the administrator is responsible for the choice of the domain name,
			the possible abuse of rights of third parties relating to the selection and the use of the domain name and carries a risk of losses relating
			to the above-mentioned abuse.</p>
			<p><b>'Registration revocation'</b> (the release of the domain name) is the removal of the information on the domain name from the Registry.</p>
			<p><b>'Domain delegation'</b> is the placement and the storage of information on the domain name and the applicable domain name servers (DNS)
			on the root servers of the applicable top-level domains. The domain delegation with a list of DNS servers indicated by the Administrator or Partner
			of Registrar authorized by Administrator (in case of .TEL domains Registrar) is possible only within the effective period of the domain name registration.<p>
			<p><b>'Registrar Transfer'</b> is the change of information about the domain name Registrar in the Registry by Registry Operator.</p>
